# 加，减，乘，除
num = 1
num += 1
print("num += 1: ", num)
num -= 1
print("num -= 1: ", num)
num *= 2
print("num *= 2: ", num)
num /= 2
print("num /= 2: ", num)

# 取整数，取模，幂运算
num = 4
num //= 2
print("num //= 2: ", num)

num = 3
num %= 2
print("num %= 2: ", num)
num **= 2
print("num **= 2: ", num)